#!/bin/bash
# 检测是否在修改前完成了根因分析

# 检查最近的对话中是否包含根因分析的关键词
if ! grep -q "根因\|根本原因\|root cause\|为什么会\|根本问题" /tmp/claude_recent_context.txt 2>/dev/null; then
    cat << 'EOF' >&2
⛔ 修改被阻断

在进行任何代码修改前，必须先完成根因分析。请先回答：

1. **问题的根本原因是什么？**（不是表面现象）
2. **为什么会发生这个错误？**（代码逻辑、数据流、外部依赖？）
3. **如何验证你的分析？**（用什么方法确认根因正确）

使用 /analyze-root-cause 命令完成分析后，再次尝试修改。
EOF
    exit 2  # 阻断修改
fi